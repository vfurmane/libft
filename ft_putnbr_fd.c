#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	unsigned int	nb;

	if (nbr < 0)
	{
		nb = nbr * -1;
		ft_putchar_fd('-', fd);
	}
	else
		nb = nbr;
	if (nb < 10)
		ft_putchar_fd(nb + '0', fd);
	else
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putchar_fd(nb % 10 + '0', fd);
	}
}
